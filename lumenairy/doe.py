# FITS FILE I/O (optional — requires astropy)
# =============================================================================

try:
    from astropy.io import fits as _fits
    _ASTROPY_AVAILABLE = True
except ImportError:
    _ASTROPY_AVAILABLE = False


def load_fits_field(filepath, hdu_amplitude=0, hdu_phase=None):
    """
    Load a complex optical field from a FITS file.

    Supports two common formats:
    - Two HDUs: amplitude in one HDU, phase in another
    - Single HDU with complex data or [real, imag] stack along first axis

    Parameters
    ----------
    filepath : str
        Path to the .fits file.
    hdu_amplitude : int, default 0
        HDU index for the amplitude array (or complex array if hdu_phase is None).
    hdu_phase : int or None, default None
        HDU index for the phase array [radians]. If None, ``hdu_amplitude``
        is assumed to contain either complex data or a [real, imag] stack.

    Returns
    -------
    E : ndarray (complex, N×N)
        Complex field.
    metadata : dict
        Selected header keywords (wavelength, dx, dy) if present.

    Notes
    -----
    Requires the ``astropy`` package. Install via ``pip install astropy``.
    """
    if not _ASTROPY_AVAILABLE:
        raise ImportError("astropy is required for FITS support. "
                          "Install with: pip install astropy")

    with _fits.open(filepath) as hdul:
        amp_data = np.array(hdul[hdu_amplitude].data)
        header = dict(hdul[hdu_amplitude].header)

        if hdu_phase is not None:
            phase_data = np.array(hdul[hdu_phase].data)
            E = amp_data * np.exp(1j * phase_data)
        elif amp_data.ndim == 3 and amp_data.shape[0] == 2:
            E = amp_data[0] + 1j * amp_data[1]
        elif np.iscomplexobj(amp_data):
            E = amp_data.astype(complex)
        else:
            # Amplitude only — assume zero phase
            E = amp_data.astype(complex)

    metadata = {
        'wavelength': header.get('WAVELENG'),
        'dx': header.get('DX') or header.get('PIXSCALE'),
        'dy': header.get('DY') or header.get('PIXSCALE'),
    }
    return E, metadata


def save_fits_field(filepath, E, wavelength=None, dx=None, dy=None,
                     metadata=None, split_amp_phase=True):
    """
    Save a complex optical field to a FITS file.

    Parameters
    ----------
    filepath : str
        Output .fits path.
    E : ndarray (complex, N×N)
        Complex field to save.
    wavelength : float, optional
        Wavelength [m]. Stored as WAVELENG header keyword.
    dx, dy : float, optional
        Grid spacings [m]. Stored as DX, DY header keywords.
    metadata : dict, optional
        Additional header keywords to include.
    split_amp_phase : bool, default True
        If True, save as two HDUs (amplitude + phase).
        If False, save as a single 3D array [real, imag, ...].

    Notes
    -----
    Requires ``astropy``. The split format (default) is more widely
    compatible with standard astronomy tools.
    """
    if not _ASTROPY_AVAILABLE:
        raise ImportError("astropy is required for FITS support. "
                          "Install with: pip install astropy")

    header = _fits.Header()
    if wavelength is not None:
        header['WAVELENG'] = (wavelength, 'Wavelength [m]')
    if dx is not None:
        header['DX'] = (dx, 'Grid spacing in x [m]')
    if dy is not None:
        header['DY'] = (dy, 'Grid spacing in y [m]')
    if metadata:
        for key, value in metadata.items():
            header[key.upper()[:8]] = value

    if split_amp_phase:
        amp = np.abs(E).astype(np.float32)
        phase = np.angle(E).astype(np.float32)

        primary = _fits.PrimaryHDU(amp, header=header)
        primary.header['EXTNAME'] = 'AMPLITUDE'

        phase_hdu = _fits.ImageHDU(phase)
        phase_hdu.header['EXTNAME'] = 'PHASE'
        phase_hdu.header['BUNIT'] = 'radians'

        _fits.HDUList([primary, phase_hdu]).writeto(filepath, overwrite=True)
    else:
        stacked = np.stack([np.real(E), np.imag(E)]).astype(np.float32)
        primary = _fits.PrimaryHDU(stacked, header=header)
        primary.header['FORMAT'] = 'real_imag_stack'
        primary.writeto(filepath, overwrite=True)
